function dipFn(multiplier, offset) {
    const spread = 0.014;
    const center = 125;
    const sharpness = 20;
    return (h) => {
        const x = Math.abs(spread * (h - center)) ** sharpness;
        return (multiplier * (x / (1 + x))) + offset;
    };
}

const lightnessEquations = [
    99,
    98,
    92,
    dipFn(13, 70),
    dipFn(28, 43),
    dipFn(28, 38),
    [dipFn(18, 35), dipFn(18, 32)],
    dipFn(11, 20),
    dipFn(8, 14),
    [7, 9],
    [4, 5],
];

// eslint-disable-next-line no-unused-vars
function modifyLightness(color, intensity, mode = 'LIGHT') {
    const intensityMap = [50, 100, 200, 300, 400, 500, 600, 700, 800, 1000];
    if (mode === 'DARK') {
        intensityMap.reverse();
    }
    let equation = lightnessEquations[intensityMap.indexOf(intensity)];
    if (_.isArray(equation)) {
        equation = equation[mode === 'LIGHT' ? 0 : 1];
    }
    if (_.isFunction(equation)) {
        return {
            ...color,
            l: equation(color.h),
        };
    }
    return {
        ...color,
        l: equation,
    };
}

